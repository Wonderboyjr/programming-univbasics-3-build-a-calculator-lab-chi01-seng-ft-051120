def addition(num1, num2)
  sum = num1 + num2
  return sum
end

def subtraction(num1, num2)
  sum = num1 - num2
  return sum
end

def division(num1, num2)
  sum = num1/num2
  return sum

end

def multiplication(num1, num2)
  sum = num1*num2
  return sum

end

def modulo(num1, num2)
  sum = num1%num2
  return sum
end

def square_root(num1, num2)
  sum1 = Math.sqrt(num1)
  sum2 = Math.sqrt(num2)
  return sum1
  return sum2
end
